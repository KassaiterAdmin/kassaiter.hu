  <h2>Pajor András</h2> 2002 óta pélbános
  <p>
      <img class="content_pic"  alt="Pajor András" src="img/pajor_andras.jpg"/>Pajor András Budapesten született, 1958. június 26-án, Pajor Sándor és Treigár Margit harmadik gyermekeként[forrás?].
      1976-ban, a Szentendrei Ferences Gimnáziumban érettségizett. Két év sorkatonai szolgálat után kezdte el teológiai tanulmányait.
      Első versei, publikációi már növendékkorában megjelentek, közben az esztergomi Magyar Egyházirodalmi Iskola elnökévé választották.
      1985. június 15-én Lékai László bíboros szentelte pappá Esztergomban. Egy évig Solymáron, további három esztendeig Óbudán volt káplán.
      <br><br>
      31 évesen plébánosi kinevezést kapott a Nógrád megyei Mohorára és Cserháthalápra, közben Balassagyarmaton a Hitoktatóképző Főiskolán filozófiát és dogmatikát tanított.
      Fiatal papként egy ideig állandó, ifjúsági levelező rovatot vezetett az Új Ember katolikus hetilapban.
      Ettől az időtől jelentek meg először ifjúsági zsebkönyvei, majd komolyabb terjedelmű tudományos és szépirodalmi művei is.
      <br><br>
      Éveken át volt szerkesztője a Kossuth Rádió Katolikus félóra című műsorának.
      1994-ben Budapestre (Cinkotára és Árpádföldre) került plébánosnak.
      Ezután néhány éven át speciális kollégiumként teológiát és bibliaismeretet tanított az ELTE Tanárképző Főiskolai Karán,
      majd Mallár Évával és Pilhál Györggyel megalapította a Prudencia Keresztény Újságíró Iskolát, ahol keresztény kultúrtörténetet, szerkesztéstant és irodalomesztétikát tanított.
      <br><br>
      Később a Pannon Rádióban jegyzetíró és egy ifjúsági műsor szerkesztő-műsorvezetője volt.
      A rádió megszűnése után a Vasárnapi Újság bízta meg rendszeres jegyzetírással.
      2003-ban Paskai László bíboros a Herminamezői Szentlélek-templom (Budapest, Kassai tér) plébánosává nevezte ki.
      2009-ben a <a href="http://kkakademia.hu">Keresztény Kulturális Akadémia</a> kuratóriuma megválasztotta országos elnökének.
