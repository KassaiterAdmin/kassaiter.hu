<h1>Bemutatkozás</h1>
<br>
<p>
    Plébániánk Budapest XIV. kerületének azon részén található, ahol sem közlekedési csomópont,
    sem egyéb technikai ösztönző erő nem segíti elő a templomi életben való tömeges részvételt.
    Akik ide járnak, azok vagy a közösséget, vagy a gyönyörű templom sajátos légkörét, vagy a liturgia felemelő erejét keresik,
    amely egy ragyogó ministráns-csapat közreműködésével emeli ki a megjelenő misztériumot.
    <br><br>
    Plébániánk mozgató rugói azok a közösségek, amelyek összetartásukkal, elmélyültebb lelki életükkel és gyakorlati segítségükkel egészítik ki lelkipásztoraink munkáját.
    Ők szervezik rendezvényeinket, kirándulásainkat, lelkigyakorlatainkat, ők segítik karitatív életünket.
    Plébániai közösségünk igyekszik az élet minden hasznos területén is híveink rendelkezésére állni.
    Családjainkat és magányos testvéreinket orvosi kapcsolatokkal, a gyermek-egészségügyi alap működtetésével,
    különféle termelők (pl. méztermelő, házi tejtermék termelő, stb.) meghívásával is segítjük.
    Ugyanakkor a főváros egyik legerőteljesebb és legszínvonalasabb keresztény kulturális élete folyik falaink között,
    főként a Keresztény Kulturális akadémia működése révén, míg kivételes hangzású orgonánk és templomi akusztikánk kihasználásával koncertsorozatainkat orgonaművész-kántorunk,
    <a href="http://www.myorganmusic.com/Orgonamuvesz/Gesztest-Toth_Laszlo.html">Dr. Gesztesi Tóth László</a> szervezi.
    <br><br>
    A többi plébániához hasonló missziós kötelezettségeinket teljesítjük, helyi sajátosságunk azonban, hogy a hozzánk tartozó,
    városligeti Jáki kápolna az ország legkeresettebb eskető helye. Évente nagyon sok jegyespár köti itt össze az életét,
    többségükben a vallásgyakorlás kezdetén járó fiatalokról van szó, akiknek ezért a szentségre való felkészítése komolyabb odafigyelést igényel.
    <br><br>
    <strong>A plébánia-épület a templom főbejáratával szemben, a park másik oldalán, a Kassai tér 2. szám alatt található.</strong>
</p>
